#Exponienciacion enlace derecho

print(-3 ** 2)                                 #Imprime resultado                   

print(-2 ** 3)                                 #Imprime resultado  

print(-(3 ** 2))                               #Imprime rsultado de operacion

#Multiplicacion y Residuo

print(2 * 3 % 5)                               #Muestra resultado de la multiplicacion

#Operadores con [arentesis]
print((5 * ((25 % 13) + 100) / (2 * 13)) // 2) #Los numeros en los parentesis se calculan primero

#Version Mejorada

# Módulo encadenado
print(9 % 6 % 2)                               # Módulo de 9 % 6 % 2 (resultado: 1)

# Exponenciación encadenada (derecha)
print(2 ** 2 ** 3)                             # Exponenciación de 2 ** 2 ** 3 (resultado: 256)

# Exponenciación con signo negativo
print(-3 ** 2)                                 # Exponenciación de -3 ** 2 (resultado: -9)

# Exponenciación con signo negativo
print(-2 ** 3)                                 # Exponenciación de -2 ** 3 (resultado: -8)

# Exponenciación con signo negativo y paréntesis
print(-(3 ** 2))                               # Exponenciación de -(3 ** 2) (resultado: -9)

# Multiplicación y módulo
print(2 * 3 % 5)                               # Multiplicación y módulo de 2 * 3 % 5 (resultado: 1)

# Operaciones con paréntesis
print((5 * ((25 % 13) + 100) / (2 * 13)) // 2) # Operación compleja con paréntesis (resultado: 10.0)

# Suma y multiplicación con paréntesis
print((10 + 5) * 2)                            # Suma y multiplicación con paréntesis (resultado: 30)

# División y módulo con paréntesis
print((20 / 4) % 3)                            # División y módulo con paréntesis (resultado: 2.0)

# Exponenciación y multiplicación con paréntesis
print((2 ** 3) * 4)                            # Exponenciación y multiplicación con paréntesis (resultado: 32)

# Operación compleja con múltiples paréntesis
print((((5 + 3) * 2) ** 2) / 4)                # Operación compleja con múltiples paréntesis (resultado: 64.0)

# Módulo y división con paréntesis
print((15 % 4) / 2)                            # Módulo y división con paréntesis (resultado: 1.5)

# Exponenciación y suma con paréntesis
print((2 ** 2) + (3 ** 2))                     # Exponenciación y suma con paréntesis (resultado: 13)

# Multiplicación y división con paréntesis
print((10 * 5) / (2 + 3))                      # Multiplicación y división con paréntesis (resultado: 10.0)

# Operación compleja con paréntesis y módulo
print((100 // 7) % 3)                          # División entera y módulo con paréntesis (resultado: 2)
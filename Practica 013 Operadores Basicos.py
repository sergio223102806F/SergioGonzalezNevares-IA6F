#Version Mejorada
# Suma de enteros
print(2 + 2)                                             # Suma de 2 + 2 (resultado: 4)

# Multiplicación de enteros
print(2 * 3)                                             # Multiplicación de 2 * 3 (resultado: 6)

# Potencia con flotante
print(2 ** 2.3)                                          # Potencia de 2 elevado a 2.3 (resultado: ~4.9246)

# División de enteros
print(6 / 3)                                             # División de 6 / 3 (resultado: 2.0)

# División con flotante
print(6 / 3.)                                            # División de 6 / 3.0 (resultado: 2.0)

# División entera (floor division) de enteros
print(6 // 3)                                            # División entera de 6 // 3 (resultado: 2)

# División entera (floor division) con flotante
print(6 // 3.)                                           # División entera de 6 // 3.0 (resultado: 2.0)

# División negativa con flotante
print(-6 / 2.23)                                         # División de -6 / 2.23 (resultado: ~-2.6906)

# División entera negativa
print(-5 // 2)                                           # División entera de -5 // 2 (resultado: -3)

# Módulo (resto de la división)
print(13 % 2)                                            # Módulo de 13 % 2 (resultado: 1)

# Suma de números negativos
print(-4 + 4)                                            # Suma de -4 + 4 (resultado: 0)

# Suma de números negativos y positivos
print(-4. + 8)                                           # Suma de -4.0 + 8 (resultado: 4.0)

# Resta de números
print(10 - 5)                                            # Resta de 10 - 5 (resultado: 5)

# Multiplicación de números negativos
print(-3 * 4)                                            # Multiplicación de -3 * 4 (resultado: -12)

# Potencia de números negativos
print((-2) ** 3)                                         # Potencia de -2 elevado a 3 (resultado: -8)

# Módulo con números negativos
print(-13 % 5)                                           # Módulo de -13 % 5 (resultado: 2)

# División con resultado flotante negativo
print(-10 / 3)                                           # División de -10 / 3 (resultado: ~-3.3333)

# División entera con resultado negativo
print(-10 // 3)                                          # División entera de -10 // 3 (resultado: -4)

# Suma de números flotantes
print(3.5 + 2.1)                                         # Suma de 3.5 + 2.1 (resultado: 5.6)

# Multiplicación de números flotantes
print(2.5 * 3.2)                                         # Multiplicación de 2.5 * 3.2 (resultado: 8.0)

# Potencia de números flotantes
print(2.5 ** 2)                                          # Potencia de 2.5 elevado a 2 (resultado: 6.25)

# Módulo con números flotantes
print(10.5 % 3)                                          # Módulo de 10.5 % 3 (resultado: 1.5)





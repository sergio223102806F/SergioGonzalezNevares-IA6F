#Version Mejorada
# Imprime un número entero como cadena
print("2")                                              # Imprime el entero "2" como cadena

# Imprime un número entero directamente
print(2)                                                # Imprime el entero 2 directamente

# Imprime un número octal (base 8)
print(0o123)                                            # Imprime el número octal 123 (83 en decimal)

# Imprime un número en notación científica
print(10E-2)                                            # Imprime 10 elevado a la -2 (0.1)

# Imprime una cadena con comillas dobles dentro
print('Me gustan "Los tacos"')                          # Imprime un diálogo con comillas dobles

# Imprime un diálogo con saltos de línea y comillas
print("\"I'm\"\n\"\"learning\"\"\n\"\"\"Python\"\"\"")  # Imprime un diálogo centrado con comillas

# Imprime un número flotante
print(3.1416)                                           # Imprime un número flotante (pi)

# Imprime un número en notación hexadecimal (base 16)
print(0x1A)                                             # Imprime el número hexadecimal 1A (26 en decimal)

# Imprime una cadena con comillas simples dentro
print("Él dijo: 'Hola'")                                # Imprime un diálogo con comillas simples

# Imprime una cadena con formato de f-string
print(f"El valor de pi es aproximadamente {3.1416}")    # Imprime una cadena formateada con f-string

# Imprime una cadena con tabulaciones
print("Nombre:\tJuan\nEdad:\t25")                       # Imprime una cadena con tabulaciones y saltos de línea

# Imprime una cadena con caracteres de escape
print("C:\\Users\\Documentos")                          # Imprime una ruta con caracteres de escape

# Imprime una cadena con formato de raw string
print(r"C:\Users\Documentos")                           # Imprime una ruta como raw string (sin escapar caracteres)

# Imprime una cadena con formato de bytes
print(b"Hello World")                                   # Imprime una cadena en formato de bytes

# Imprime una cadena con formato de Unicode
print("\u0048\u0065\u006C\u006C\u006F")                # Imprime "Hello" usando códigos Unicode